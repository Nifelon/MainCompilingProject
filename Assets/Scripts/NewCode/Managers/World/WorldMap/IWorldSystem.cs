namespace Game.Core
{
    /// Контракт любой подсистемы мира.
    /// Требования:
    /// - Initialize(...) идемпотентен (повторный вызов безопасен).
    /// - Порядок инициализации задаётся числом Order: меньше — раньше.
    public interface IWorldSystem
    {
        /// Порядок инициализации в рамках своей "матрёшки".
        int Order { get; }

        /// Запуск/пересборка системы. Никакой тяжёлой логики в Awake/Start —
        /// всё должно выполняться здесь, по вызову WorldManager.
        void Initialize(WorldContext ctx);
    }
}
